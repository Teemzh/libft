#include "libft.h"

int ft_lstsize(t_list *lst)
{
    t_list *lsst;
    int i = 0;
    
    lsst = lst;
    while (lsst != NULL)
    {
        lsst = lsst -> next;
        i++;
    }
    return (i);
}